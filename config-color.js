    findPantone: function(code) {
        // Normalizar el c√≥digo (convertir a min√∫sculas y asegurar formato)
        const normalizedCode = code.toLowerCase().trim();
        
        // Buscar en met√°licos primero
        if (this.pantoneMetallic[normalizedCode]) {
            return {
                ...this.pantoneMetallic[normalizedCode],
                type: 'metallic'
            };
        }
        
        // Buscar en coated
        if (this.pantoneCoated[normalizedCode]) {
            return {
                ...this.pantoneCoated[normalizedCode],
                type: 'coated'
            };
        }
        
        return null;
    },

